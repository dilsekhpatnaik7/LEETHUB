//User function Template for C++
class Solution
{
public:
    vector<long long> factorialNumbers(long long N) {
        vector<long long> ans;
        long long counter = 2, factorial = 1;
        while(factorial <= N) {
            ans.push_back(factorial);
            factorial *= counter;
            counter++;
        }
        return ans;
    }
};

//{ Driver Code Starts.
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        long long N;
        cin>>N;
        Solution ob;
        vector<long long> ans = ob.factorialNumbers(N);
        for(auto num : ans){
            cout<<num<<" ";
        }
        cout<<endl;
        
    }
    return 0;
}
