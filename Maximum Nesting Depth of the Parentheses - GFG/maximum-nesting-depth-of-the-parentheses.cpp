class Solution {
  public:
    int maxDepth(string s) {
        int ans = INT_MIN, count = 0;
        for(auto ch: s) {
            if(ch == '(') count++;
            else if(ch == ')') count--;
            
            ans = max(ans, count);
        }
        return ans;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        string s;
        cin >> s;

        Solution obj;
        int ans = obj.maxDepth(s);
        cout << ans << endl;
    }
    return 0;
}
